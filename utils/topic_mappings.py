def get_cluster_name(content_type, category, cluster_id):
    """
    클러스터 ID에 해당하는 이름 반환
    
    Args:
        content_type: 'movie' or 'drama'
        category: 'hit' or 'flop'
        cluster_id: int
    
    Returns:
        str: 클러스터명
    """
    mapping_key = f"{content_type.upper()}_{category.upper()}_CLUSTER_NAMES"
    mapping = globals().get(mapping_key, {})
    return mapping.get(cluster_id, f"Cluster {cluster_id}")


def get_topic_name(content_type, category, topic_id):
    """
    토픽 ID에 해당하는 이름 반환
    
    Args:
        content_type: 'movie' or 'drama'
        category: 'hit' or 'flop'
        topic_id: int
    
    Returns:
        str: 토픽명
    """
    mapping_key = f"{content_type.upper()}_{category.upper()}_TOPIC_NAMES"
    mapping = globals().get(mapping_key, {})
